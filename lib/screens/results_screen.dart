class ResultsScreen extends StatefulWidget {
  final TileCalculation calculation;

  const ResultsScreen({super.key, required this.calculation});

  @override
  State<ResultsScreen> createState() => _ResultsScreenState();
}

class _ResultsScreenState extends State<ResultsScreen>
    with TickerProviderStateMixin {
  late AnimationController _heroAnim;
  late AnimationController _listAnim;
  late List<Animation<double>> _itemAnims;

  static const _itemCount = 6;

  @override
  void initState() {
    super.initState();
    _heroAnim = AnimationController(
      vsync: this,
      duration: const Duration(milliseconds: 500),
    )..forward();

    _listAnim = AnimationController(
      vsync: this,
      duration: const Duration(milliseconds: 800),
    )..forward();

    _itemAnims = List.generate(_itemCount, (i) {
      final start = (i * 0.12).clamp(0.0, 1.0);
      final end = (start + 0.4).clamp(0.0, 1.0);
      return CurvedAnimation(
        parent: _listAnim,
        curve: Interval(start, end, curve: Curves.easeOutCubic),
      );
    });
  }

  @override
  void dispose() {
    _heroAnim.dispose();
    _listAnim.dispose();
    super.dispose();
  }

  TileCalculation get c => widget.calculation;
  NumberFormat get _nf =>
      NumberFormat.currency(symbol: c.currencySymbol, decimalDigits: 2);
  String _fmt(double v) => _nf.format(v);

  void _shareText() {
    final text = '''
Tilemate Estimate — ${c.roomName}
────────────────────────
Floor area:     ${c.floorArea.toStringAsFixed(2)} m²
Tile size:      ${c.tileLength.toStringAsFixed(0)}×${c.tileWidth.toStringAsFixed(0)}${c.tileUnit.label}
Pattern:        ${c.layoutPattern.displayName}
Grout joint:    ${c.groutJointMm.toStringAsFixed(1)} mm
Total wastage:  ${c.totalWastagePercent.toStringAsFixed(0)}%

TILES REQUIRED
Tiles needed:   ${c.tilesNeeded}
Waste tiles:    ${c.wasteTiles}
Total tiles:    ${c.totalTilesRequired}
Boxes to buy:   ${c.boxesRequired} boxes (${c.tilesPerBox}/box)
Spare tiles:    ${c.spareTiles}
Cut tiles est.: ${c.estimatedCutTiles}

COST BREAKDOWN
Tiles:          ${_fmt(c.tileCost)}
Labour:         ${_fmt(c.laborCost)}
Grout:          ${_fmt(c.groutCost)}
Other:          ${_fmt(c.otherCost)}
────────────────────────
TOTAL:          ${_fmt(c.totalCost)}
────────────────────────
Generated by Tilemate
    '''.trim();

    Share.share(text, subject: 'Tilemate: ${c.roomName} Estimate');
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      backgroundColor: AppTheme.bg,
      body: CustomScrollView(
        slivers: [
          // ── Hero total ─────────────────────────────────────────────────────
          SliverAppBar(
            pinned: true,
            backgroundColor: AppTheme.bg,
            expandedHeight: 200,
            leading: IconButton(
              icon: const Icon(Icons.arrow_back),
              onPressed: () => Navigator.of(context).pop(),
            ),
            actions: [
              IconButton(
                icon: const Icon(Icons.share_outlined),
                onPressed: _shareText,
                tooltip: 'Share estimate',
              ),
              IconButton(
                icon: const Icon(Icons.folder_open_outlined),
                onPressed: () => Navigator.of(context).push(
                  MaterialPageRoute(
                    builder: (_) =>
                        AddToProjectScreen(calculation: c),
                  ),
                ),
                tooltip: 'Save to project',
              ),
            ],
            flexibleSpace: FlexibleSpaceBar(
              background: SlideTransition(
                position: Tween<Offset>(
                  begin: const Offset(0, -0.2),
                  end: Offset.zero,
                ).animate(CurvedAnimation(
                  parent: _heroAnim,
                  curve: Curves.easeOutCubic,
                )),
                child: FadeTransition(
                  opacity: _heroAnim,
                  child: _HeroBanner(c: c),
                ),
              ),
            ),
          ),

          // ── Results body ───────────────────────────────────────────────────
          SliverPadding(
            padding: const EdgeInsets.fromLTRB(16, 8, 16, 40),
            sliver: SliverList(
              delegate: SliverChildListDelegate([
                // Tile counts
                _AnimatedSection(
                  animation: _itemAnims[0],
                  child: _TileCountCard(c: c),
                ),
                const SizedBox(height: 12),

                // Area & tile spec
                _AnimatedSection(
                  animation: _itemAnims[1],
                  child: _SpecCard(c: c),
                ),
                const SizedBox(height: 12),

                // Cost breakdown
                _AnimatedSection(
                  animation: _itemAnims[2],
                  child: _CostBreakdownCard(c: c, fmt: _fmt),
                ),
                const SizedBox(height: 12),

                // Grout
                if (c.groutCost > 0 || c.groutColor != null)
                  _AnimatedSection(
                    animation: _itemAnims[3],
                    child: _GroutCard(c: c, fmt: _fmt),
                  ),
                const SizedBox(height: 12),

                // Wastage breakdown
                _AnimatedSection(
                  animation: _itemAnims[4],
                  child: _WastageCard(c: c),
                ),
                const SizedBox(height: 20),

                // Action buttons
                _AnimatedSection(
                  animation: _itemAnims[5],
                  child: Column(
                    children: [
                      TmAmberButton(
                        label: 'Save to Project',
                        icon: Icons.folder_open_outlined,
                        onPressed: () => Navigator.of(context).push(
                          MaterialPageRoute(
                            builder: (_) =>
                                AddToProjectScreen(calculation: c),
                          ),
                        ),
                      ),
                      const SizedBox(height: 10),
                      SizedBox(
                        width: double.infinity,
                        child: OutlinedButton.icon(
                          onPressed: _shareText,
                          icon: const Icon(Icons.share_outlined, size: 16),
                          label: const Text('Share Estimate'),
                        ),
                      ),
                    ],
                  ),
                ),
              ]),
            ),
          ),
        ],
      ),
    );
  }
}

