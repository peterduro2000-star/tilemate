// ── Cost Breakdown Card ───────────────────────────────────────────────────────

class _CostBreakdownCard extends StatelessWidget {
  final TileCalculation c;
  final String Function(double) fmt;

  const _CostBreakdownCard({required this.c, required this.fmt});

  @override
  Widget build(BuildContext context) {
    final items = [
      ('Tiles (${c.boxesRequired} boxes)', c.tileCost),
      ('Labour', c.laborCost),
      if (c.groutCost > 0) ('Grout', c.groutCost),
      if (c.otherCost > 0) ('Other', c.otherCost),
    ];

    return TmCard(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const TmSectionLabel('Cost Breakdown'),
          const SizedBox(height: 4),
          ...items.map((item) => _CostRow(
                label: item.$1,
                value: fmt(item.$2),
                faint: item.$2 == 0,
              )),
          const TmDivider(),
          Row(
            mainAxisAlignment: MainAxisAlignment.spaceBetween,
            children: [
              const Text(
                'TOTAL',
                style: TextStyle(
                  fontSize: 13,
                  fontWeight: FontWeight.w900,
                  letterSpacing: 0.5,
                  color: AppTheme.textHigh,
                ),
              ),
              Text(
                fmt(c.totalCost),
                style: const TextStyle(
                  fontSize: 20,
                  fontWeight: FontWeight.w900,
                  letterSpacing: -0.5,
                  color: AppTheme.amber,
                ),
              ),
            ],
          ),
          const SizedBox(height: 8),
          Text(
            '${fmt(c.totalCost / c.floorArea)} per m²',
            style: const TextStyle(fontSize: 11, color: AppTheme.textLow),
          ),
        ],
      ),
    );
  }
}

// ── Grout Card ────────────────────────────────────────────────────────────────

class _GroutCard extends StatelessWidget {
  final TileCalculation c;
  final String Function(double) fmt;

  const _GroutCard({required this.c, required this.fmt});

  @override
  Widget build(BuildContext context) {
    int bags = 0;
    if (c.groutBagCoverage != null && c.groutBagCoverage! > 0) {
      bags = (c.floorArea / c.groutBagCoverage!).ceil();
    }

    return TmCard(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const TmSectionLabel('Grout'),
          if (c.groutColor != null)
            _SpecRow('Colour', c.groutColor!),
          if (bags > 0) _SpecRow('Bags needed', '$bags bags'),
          if (c.groutCost > 0) _SpecRow('Grout cost', fmt(c.groutCost)),
        ],
      ),
    );
  }
}

// ── Wastage Card ──────────────────────────────────────────────────────────────

class _WastageCard extends StatelessWidget {
  final TileCalculation c;
  const _WastageCard({required this.c});

  @override
  Widget build(BuildContext context) {
    return TmCard(
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const TmSectionLabel('Wastage Detail'),
          const SizedBox(height: 4),
          _SpecRow('Pattern wastage (${c.layoutPattern.displayName})',
              '${c.patternWastagePercent.toStringAsFixed(0)}%'),
          _SpecRow('Extra buffer',
              '+${c.extraBufferPercent.toStringAsFixed(0)}%'),
          _SpecRow('Total wastage', '${c.totalWastagePercent.toStringAsFixed(0)}%'),
          const SizedBox(height: 10),
          // Visual wastage bar
          _WastageBar(
            patternPct: c.patternWastagePercent,
            bufferPct: c.extraBufferPercent,
          ),
        ],
      ),
    );
  }
}

class _WastageBar extends StatelessWidget {
  final double patternPct;
  final double bufferPct;

  const _WastageBar({required this.patternPct, required this.bufferPct});

  @override
  Widget build(BuildContext context) {
    final maxDisplay = 30.0;
    final patternFrac = (patternPct / maxDisplay).clamp(0.0, 1.0);
    final bufferFrac = ((patternPct + bufferPct) / maxDisplay).clamp(0.0, 1.0);

    return LayoutBuilder(builder: (context, constraints) {
      final w = constraints.maxWidth;
      return Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Stack(
            children: [
              Container(
                height: 8,
                width: w,
                decoration: BoxDecoration(
                  color: AppTheme.surfaceAlt,
                  borderRadius: BorderRadius.circular(4),
                ),
              ),
              Container(
                height: 8,
                width: w * bufferFrac,
                decoration: BoxDecoration(
                  color: AppTheme.amber.withValues(alpha: 0.4),
                  borderRadius: BorderRadius.circular(4),
                ),
              ),
              Container(
                height: 8,
                width: w * patternFrac,
                decoration: BoxDecoration(
                  color: AppTheme.amber,
                  borderRadius: BorderRadius.circular(4),
                ),
              ),
            ],
          ),
          const SizedBox(height: 6),
          Row(
            children: [
              _BarLegend(color: AppTheme.amber, label: 'Pattern'),
              const SizedBox(width: 14),
              _BarLegend(
                  color: AppTheme.amber.withValues(alpha: 0.4), label: 'Buffer'),
            ],
          ),
        ],
      );
    });
  }
}

class _BarLegend extends StatelessWidget {
  final Color color;
  final String label;
  const _BarLegend({required this.color, required this.label});

  @override
  Widget build(BuildContext context) {
    return Row(
      mainAxisSize: MainAxisSize.min,
      children: [
        Container(width: 10, height: 10, color: color),
        const SizedBox(width: 4),
        Text(label,
            style: const TextStyle(fontSize: 10, color: AppTheme.textLow)),
      ],
    );
  }
}

// ── Shared row widgets ────────────────────────────────────────────────────────

class _SpecRow extends StatelessWidget {
  final String label;
  final String value;

  const _SpecRow(this.label, this.value);

  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.symmetric(vertical: 5),
      child: Row(
        children: [
          Expanded(
            child: Text(label,
                style: const TextStyle(
                    fontSize: 13, color: AppTheme.textMid)),
          ),
          Text(value,
              style: const TextStyle(
                fontSize: 13,
                fontWeight: FontWeight.w700,
                color: AppTheme.textHigh,
              )),
        ],
      ),
    );
  }
}

class _CostRow extends StatelessWidget {
  final String label;
  final String value;
  final bool faint;

  const _CostRow({
    required this.label,
    required this.value,
    this.faint = false,
  });

  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.symmetric(vertical: 5),
      child: Row(
        children: [
          Expanded(
            child: Text(label,
                style: TextStyle(
                    fontSize: 13,
                    color: faint ? AppTheme.textLow : AppTheme.textMid)),
          ),
          Text(value,
              style: TextStyle(
                fontSize: 13,
                fontWeight: FontWeight.w700,
                color: faint ? AppTheme.textLow : AppTheme.textHigh,
              )),
        ],
      ),
    );
  }
}

// ── Animated list item ────────────────────────────────────────────────────────

class _AnimatedSection extends StatelessWidget {
  final Animation<double> animation;
  final Widget child;

  const _AnimatedSection({required this.animation, required this.child});

  @override
  Widget build(BuildContext context) {
    return AnimatedBuilder(
      animation: animation,
      builder: (context, child) => FadeTransition(
        opacity: animation,
        child: SlideTransition(
          position: Tween<Offset>(
            begin: const Offset(0, 0.15),
            end: Offset.zero,
          ).animate(animation),
          child: child,
        ),
      ),
      child: child,
    );
  }
}
